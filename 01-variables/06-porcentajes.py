# Programa que permita calcular el 30%, el 60% y el 90% de cualquier número

a = float(input("Ingrese el número que desea calcularle los porcentajes: "))
porcentaje = a*0.3
print(f"El 30% de {a} es {porcentaje}")
print(f"El 60% de {a} es {porcentaje*2}")
print(f"El 90% de {a} es {porcentaje*3}")